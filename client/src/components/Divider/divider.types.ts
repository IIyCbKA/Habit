/*
--------------DividerOrientation type--------------
The type DividerOrientation specifies variant a set of styles for divider.
By default, type is horizontal.
*/
export type DividerOrientation = "horizontal" | "vertical";

/*
--------------DividerVariant type--------------
The type DividerVariant specifies variant a set of styles for divider.
By default, type is fullwidth.
*/
export type DividerVariant = "fullwidth" | "inset" | "middle";
